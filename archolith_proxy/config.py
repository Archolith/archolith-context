"""Application configuration via pydantic-settings.

Required env vars are validated on first access. get_settings() returns
a cached singleton — Settings() is constructed once per process.

Runtime overrides from PATCH /admin/config are persisted to
config_overrides.json and re-applied on startup, making them
survive restarts.

Config loading is synchronous and must complete before async request
paths begin. The module-level lock guards the first-access race condition.
"""

from __future__ import annotations

import json
import threading
from contextvars import ContextVar
from pathlib import Path

from pydantic import field_validator
from pydantic_settings import BaseSettings, SettingsConfigDict

# Module-level singleton
_settings: Settings | None = None

# Per-session effective settings overlay. When set (inside a request bound to a
# session that has config_overrides), get_settings() returns this instead of the
# global singleton, so all ~54 get_settings() call sites become session-aware
# transparently. Default None == today's behavior (global singleton).
_session_settings_ctx: ContextVar["Settings | None"] = ContextVar(
    "session_settings", default=None
)

# Fields a per-session X-Session-Config header may NOT override. /v1/chat/completions
# is unauthenticated, so a client must not be able to redirect upstream traffic or
# override secrets/paths for their session. Everything else is settable per-session.
SESSION_CONFIG_DENYLIST: frozenset[str] = frozenset({
    "upstream_base_url",
    "upstream_api_url",
    "upstream_api_key",
    "extractor_base_url",
    "extractor_api_key",
    "curator_base_url",
    "curator_api_key",
    "embedding_base_url",
    "embedding_api_key",
    "memory_api_url",
    "memory_api_key",
    "admin_token",
    "ladybug_db_path",
})

# Resolve .env relative to this file's directory (archolith_proxy/),
# then walk up one level to the project root. This ensures .env loads
# correctly regardless of the process working directory.
_PROJECT_ROOT = Path(__file__).parent.parent
_ENV_FILE = str(_PROJECT_ROOT / ".env")
_OVERRIDES_FILE = _PROJECT_ROOT / "config_overrides.json"

# In-memory snapshot of base env values (before overrides applied)
_base_values: dict[str, object] = {}

# Lock guarding _settings and _base_values during initialization
_settings_lock = threading.Lock()


